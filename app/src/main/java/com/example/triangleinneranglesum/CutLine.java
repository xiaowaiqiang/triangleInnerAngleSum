package com.example.triangleinneranglesum;

public class CutLine {
    private float sx,sy,ex,ey;

    public float getSx() {
        return sx;
    }

    public void setSxy(float sx,float sy) {
        this.sx = sx;
        this.sy = sy;

    }

    public float getSy() {
        return sy;
    }

    public float getEx() {
        return ex;
    }

    public void setExy(float ex,float ey) {
        this.ex = ex;
        this.ey = ey;

    }

    public float getEy() {
        return ey;
    }

}
